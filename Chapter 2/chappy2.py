# while statement executes code over and over again as long as statement is true
# The while keyword
# A condition (that is, an expression that evaluates to True or False)
# A colon
# Starting on the next line, an indented block of code (called the while clause)
#while clause is often called the loop
spam = 0
if spam < 5:
    print ('Hello world.')
    spam = spam + 1

spam = 0
while spam < 5:
    print ('Hello, world.')
    spam = spam + 1

# The first time the condition is found to be False, the while clause is skipped.


